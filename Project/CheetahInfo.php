                <h2 class="text-h2 font-bold theme-dark:text-foreground-td theme-medium:text-foreground-tm text-foreground text-pretty ">How do parrots help their ecosystems?</h2>
                    <p data-block-key="csj8h">- Parrots play a vital role in maintaining the health of their forests by dispersing seeds through their droppings, which promotes plant growth and biodiversity. </p>
                    <p data-block-key="0zfsg">- The habitats of parrots are also important for local communities for food, income and fuel for cooking and heating; they also play a crucial role in regulating water flow..</p>
                    <p data-block-key="0zfsg">- Parrots live in the forested areas of tropical regions. The forests act as natural drainage basins, helping to control water runoff, reduce soil erosion and maintain water quality, which over a half a billion people depend on..</p>
                    <p data-block-key="0zfsg">- Parrot habitats rival the highest biodiversity of any ecosystem in the world. It's also vitally important for other threatened and endangered species, including golden snub-nosed monkeys, takins, red pandas and snow leopards.  </p>
                    <p data-block-key="0zfsg">- By protecting parrots we’re also helping protect the wider environment where they live, for all the wildlife and the people that depend on it. </p>
